package bug.frontstage.program.model;

public class Program {

	private int program_id;
	private int buser_id;
	private String program_name;
	private String program_creator;
	private String program_pate;
	private String program_describe;
	private String program_produce;
	private String program_type;
	private String program_block;
	public int getProgram_id() {
		return program_id;
	}
	public void setProgram_id(int program_id) {
		this.program_id = program_id;
	}
	public int getBuser_id() {
		return buser_id;
	}
	public void setBuser_id(int buser_id) {
		this.buser_id = buser_id;
	}
	public String getProgram_name() {
		return program_name;
	}
	public void setProgram_name(String program_name) {
		this.program_name = program_name;
	}
	public String getProgram_creator() {
		return program_creator;
	}
	public void setProgram_creator(String program_creator) {
		this.program_creator = program_creator;
	}
	public String getProgram_pate() {
		return program_pate;
	}
	public void setProgram_pate(String program_pate) {
		this.program_pate = program_pate;
	}
	public String getProgram_describe() {
		return program_describe;
	}
	public void setProgram_describe(String program_describe) {
		this.program_describe = program_describe;
	}
	public String getProgram_produce() {
		return program_produce;
	}
	public void setProgram_produce(String program_produce) {
		this.program_produce = program_produce;
	}
	public String getProgram_type() {
		return program_type;
	}
	public void setProgram_type(String program_type) {
		this.program_type = program_type;
	}
	public String getProgram_block() {
		return program_block;
	}
	public void setProgram_block(String program_block) {
		this.program_block = program_block;
	}
}
